    void setID()
    {   try
        {   st=con.createStatement();
            rs=st.executeQuery("SELECT MAX(ID) FROM Marca;");
            if(rs.next())
            {   if(rs.getString(1).compareTo("")==0)
                    jTextField1.setText("M0001");
                else 
                {   int corelativo=Integer.parseInt(rs.getString(1).substring(1));
                    String id="";
                    if(corelativo<9) id="M000"+(corelativo+1);
                    else if(corelativo>=9) id="M00"+(corelativo+1);
                    else if(corelativo>=99) id="M0"+(corelativo+1);
                    jTextField1.setText( id );
                }
            }
        }
        catch(SQLException e){  JOptionPane.showMessageDialog(this, "Error Debido a : "+e.toString());}
    }
    
