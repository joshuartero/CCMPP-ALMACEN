    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!camposVacios())
        {   int opc=JOptionPane.showConfirmDialog(this, "Desea Registrar Esta Marca?", "CONFIRMAR REGISTRO", JOptionPane.YES_NO_OPTION);
            if(opc==JOptionPane.YES_OPTION)
               registrar();
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

    boolean camposVacios()
    {   boolean vacios=false;
        if(jTextField2.getText().length()==0)
        {   JOptionPane.showMessageDialog(this, "Porfavor Ingrese Marca");
            jTextField2.requestFocus();
            vacios=true;
        }
        return vacios;
    }
    
    void registrar()
    {   try
        {   st=con.createStatement();
            st.executeUpdate("INSERT INTO Marca VALUES ('"+jTextField1.getText()+"','"+jTextField2.getText()+"');");
            JOptionPane.showMessageDialog(this, "Marca Registrada !");
            dispose();
        }
        catch(SQLException e){  JOptionPane.showMessageDialog(this, "Error Debido a : "+e.toString());}
    }
    
