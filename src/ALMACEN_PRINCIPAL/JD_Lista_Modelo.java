    private void jTextField1CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jTextField1CaretUpdate
        try
        {   rs=st.executeQuery("SELECT * FROM Modelo Where Modelo Like '%"+jTextField1.getText()+"%'");
            limpiarTabla();
            while(rs.next())
            {   Object rowData[]={rs.getString(1), rs.getString(2)};
                modelo.addRow(rowData);
            }
        }
        catch(SQLException e)   {   JOptionPane.showMessageDialog(this,"Error debido a: "+e.toString());}
    }//GEN-LAST:event_jTextField1CaretUpdate

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if(evt.getClickCount()==2)
        {   jddh.jComboBox2.removeAllItems();
            jddh.cargarModelo();
            jddh.jComboBox2.setSelectedItem(jTable1.getValueAt(jTable1.getSelectedRow(), 1)+"");
            dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked

    void limpiarTabla()
    {   int a = jTable1.getRowCount()-1;
        for (int i = a; i >= 0; i--)           
            modelo.removeRow(jTable1.getRowCount()-1);        
    }
    
    void cargarModelos()
    {   try
        {   st=con.createStatement();
            rs=st.executeQuery("Select MO.ID, MO.`MODELO` from Modelo MO INNER JOIN MARCA MA ON MO.`IDMARCA`=MA.`ID` WHERE MA.`MARCA`='"+jLabel3.getText()+"';");
            while(rs.next())
            {   Object rowData[]={rs.getString(1), rs.getString(2)};
                modelo.addRow(rowData);
            }
        }
        catch(SQLException e)   {   JOptionPane.showMessageDialog(this,"Error debido a: "+e.toString());}    
    }
    
